import type { Size, Task } from '../types'

/** Derive a sensible default size from the asset count (seed data only). */
function sizeFromAssets(total: number): Size {
  if (total < 8) return 'XS'
  if (total < 14) return 'S'
  if (total < 20) return 'M'
  if (total < 28) return 'L'
  return 'XL'
}

